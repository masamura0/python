    response = requests.get(url)
    web_content = response.text

    # ウェブサイトから取得したデータを解析
    local_soup = BeautifulSoup(web_content, 'html.parser')

    # ターゲットの要素を取得
    company_name = local_soup.find('h1', class_='p-profile-canopy__name').text if local_soup.find('h1', class_='p-profile-canopy__name') else 'データなし'
