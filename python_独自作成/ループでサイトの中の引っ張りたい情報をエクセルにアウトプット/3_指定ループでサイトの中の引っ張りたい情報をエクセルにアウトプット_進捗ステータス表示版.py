    capital = local_soup.find('th', text='資本金').find_next('td').text if local_soup.find('th', text='資本金') else 'データなし'
    sales = local_soup.find('th', text='売上高').find_next('td').text if local_soup.find('th', text='売上高') else 'データなし'
